    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        try {
            if (txtNazivFirme.getText().isEmpty() || txtIndustrija.getText().isEmpty()
                    || txtImeVlasnika.getText().isEmpty()
                    || txtKontakt.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!");
                return;
            }

            String nazivFirme = txtNazivFirme.getText();
            String grad = (String) cmbGrad.getSelectedItem();
            String industrija = txtIndustrija.getText();
            String imeVlasnika = txtImeVlasnika.getText();
            String kontakt = txtKontakt.getText();

            Klijent k = new Klijent(null, nazivFirme, grad, industrija, imeVlasnika, kontakt);

            ClientController.getInstance().addKlijent(k);
            MainForm mf = (MainForm) getParent();
            mf.popuniKlijente();
	    JOptionPane.showMessageDialog(this, "Uspesno dodat novi klijent!");
            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnDodajActionPerformed

